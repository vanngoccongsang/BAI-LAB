SELECT UPPER(NHANVIEN.HONV) AS HONV, 
	LOWER(NHANVIEN.TENLOT) AS TENLOT, LOWER(SUBSTRING(NHANVIEN.TENNV, 1, 1)) 
	+ UPPER(SUBSTRING(NHANVIEN.TENNV, 2, 1)) 
    + SUBSTRING(NHANVIEN.TENNV, 3, 10) AS TENNV ,NHANVIEN.DCHI
FROM NHANVIEN INNER JOIN THANNHAN ON NHANVIEN.MANV = THANNHAN.MA_NVIEN
GROUP BY NHANVIEN.HONV, NHANVIEN.TENLOT, NHANVIEN.TENNV
HAVING (COUNT(THANNHAN.MA_NVIEN) > 2)

