SELECT DEAN.TENDEAN, CAST(SUM(PHANCONG.THOIGIAN) AS decimal(5, 2)) AS [Tổng giờ làm]
FROM DEAN INNER JOIN PHANCONG ON DEAN.MADA = PHANCONG.MADA
GROUP BY DEAN.TENDEAN

